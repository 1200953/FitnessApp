);
CREATE TABLE "APP".FOODTABLE
(
    ID INTEGER NOT NULL,
    FOODNAME VARCHAR(50),
    CATEGORY VARCHAR(50),
    CALAMOUNT INTEGER,
    SERVINGUNIT VARCHAR(20),
    SERVINGAMOUNT INTEGER,
    FAT INTEGER,
    PRIMARY KEY (ID)
);
CREATE TABLE "APP".CREDENTIALTABLE
(
    ID INTEGER NOT NULL,
    USERNAME VARCHAR (50),
    PASSWDHASH VARCHAR (300),
    SIGNUPDATE DATE,
    PRIMARY KEY (ID)
);
CREATE TABLE "APP".USERTABLE
(
    ID INTEGER NOT NULL,
    NAME VARCHAR(50),
    SURNNAME VARCHAR(50),
    EMAIL VARCHAR(50),
    DOB DATE,
    HEIGHT INTEGER,
    WEIGHT INTEGER,
    GENDER VARCHAR(1),
    ADDRESS VARCHAR(80),
    POSTCODE VARCHAR(4),
    LEVELOFFACTIVITY INTEGER,
    CHECK (LEVELOFFACTIVITY >=1 AND LEVELOFFACTIVITY <=5),
    STEPSPERMILE INTEGER,
    PRIMARY KEY (ID)
);

